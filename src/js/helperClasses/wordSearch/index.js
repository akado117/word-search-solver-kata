

export default class WordSearch {
    _wordGrid = [];
    _width = null;
    _height = null;
    _wordArray = [];

    static parseWordSearchString(searchString) {
        const baseConfig = {
            _wordGrid: [],
            _width: null,
            _height: null,
            _wordArray: [],
        };
        if (typeof searchString !== 'string') return baseConfig;

    }
}